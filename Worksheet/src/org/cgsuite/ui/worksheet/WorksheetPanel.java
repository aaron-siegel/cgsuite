    @Override
    public void insertUpdate(DocumentEvent e)
    {
        if (seekingCommand)
        {
            seekingCommand = false;     // It just got inserted
        }
        else
        {
            commandHistoryPrefix = null;
        }
    }

    @Override
    public void removeUpdate(DocumentEvent e)
    {
        if (!seekingCommand)
        {
            commandHistoryPrefix = null;
        }
    }

